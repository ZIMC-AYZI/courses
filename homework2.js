// Если вариантов несколько , соответственно программа вернет все варианты.
//     Если вариантов на сегодняшний вечер нету показать варианты ближайшего рейса.
//     ПРИМЕР Я ВВОЖУ AX2022 и в следующий промт я ввожу 20:00 : результат будет Ваш терминал C ворота номер N67