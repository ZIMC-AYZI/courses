// Терминал B работает с 03:00 (по GMT) до 21:00
// Терминал C работает 24/7;
// В каждом терминале есть свои ворота для прохода на рейс самолета
// Втерминале A:
//     G1 (время работы с 06:00 до 10:00 ),
// G2 (время работы с 10:00 до 14:00),
