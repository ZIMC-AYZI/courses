// S32 (время работы с 20:00 до 21:00 )
// Втерминале C:
//     N43 (с 06:00 до 14:00),
// N35 (с 14:00 до 20:00),
// N67 (с 20:00 до 06:00)
//
// Виды самолетов на стоянке в аеропорту: AX2022, GL8000, UK20B
//
// Терминал A принимают только AX2022, UK20B
// Терминал B принимают только GL8000
// Терминалы C принимают только AX2022
//
// На входе в аеропорт стоит Стасик у которого есть программа позволяющая:
